 * @mixin \Eloquent
 */
class Favorite extends Model
{
    protected $table = 'favorites';
    
    protected $fillable = [
        'user_id',
        'document_id',
    ];

    public function user()
    {
        return $this->belongsTo(User::class);
    }

    public function document()
    {
        return $this->belongsTo(Document::class);
    }
}

